import UIKit

extension UIColor {
    struct custom {
        struct orange {
            static let lightorange = UIColor(netHex: 0xffa900)
            static let darkorange = UIColor(netHex: 0xff9800)
        }
        
        struct red {
            static let maroon = UIColor(netHex: 0x800000)
            
            
        }
    }
    
}


